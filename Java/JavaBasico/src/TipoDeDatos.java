public class TipoDeDatos {
    public static void main(String[] args) {
        // EN JAVA TENEMOS 8 TIPOS DE DATOS PRIMITIVOS
        byte numeroMini = 100;
        short numeroMediano = 32000;
        int numeroEstandar = 24;
        long numeroGrande = 1000000;

        float numeroDecimal = 1556.52f;
        double numeroDecimalEntero = 6465464454.65645;

        boolean verdad = true;
        boolean falso = false;

        char letra = 'a';  // comillas simples

        // DATO ESTRUCTURADO

        // String- no se puede modificar
        String nombre = "Angel";


        System.out.println(numeroMini);
        System.out.println(numeroMediano);
        System.out.println(numeroEstandar);
        System.out.println(numeroGrande);

        System.out.println(numeroDecimal);
        System.out.println(numeroDecimalEntero);

        System.out.println(verdad);
        System.out.println(falso);

        System.out.println(letra);

        System.out.println(nombre);



    }
}
